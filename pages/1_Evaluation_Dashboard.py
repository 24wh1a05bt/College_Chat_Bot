"""
Evaluation Dashboard - Phase 5 deliverable.

Shows the eight-dimension test report generated by run_evaluation.py:
summary stats, per-dimension pass/fail cards, failed-test drill-downs
with root cause and fix, and RAGAS metric bars.
"""
import json

import streamlit as st

from src import config

st.set_page_config(page_title="Evaluation Dashboard", page_icon="📊", layout="wide")
st.title("📊 Evaluation Dashboard")
st.caption("Eight-dimension test report for the BVRIT FAQ Chatbot")

report_path = config.EVAL_DIR / "evaluation_report.json"

if not report_path.exists():
    st.warning(
        "No evaluation report found yet. Generate one from the project root with:\n\n"
        "```\npython run_evaluation.py\n```"
    )
    st.stop()

with open(report_path) as f:
    report = json.load(f)

s = report["summary"]

# --- Summary banner --------------------------------------------------------
c1, c2, c3, c4, c5 = st.columns(5)
c1.metric("Total test cases", s["total_test_cases"])
c2.metric("Passed", s["passed"])
c3.metric("Failed", s["failed"])
c4.metric("Warning", s["warning"])
c5.metric("Pass rate", f"{s['pass_rate_pct']}%")

st.info(
    f"**Weakest dimension:** {report['weakest_dimension']}  \n"
    f"**Recommended fix:** {report['recommended_fix']}"
)

st.divider()

# --- Per-dimension cards (4x2 grid) -----------------------------------
st.subheader("Per-dimension results")
dims = list(report["per_dimension"].items())
for row_start in range(0, len(dims), 4):
    cols = st.columns(4)
    for col, (dim_name, data) in zip(cols, dims[row_start:row_start + 4]):
        with col:
            st.markdown(f"**{dim_name}**")
            st.markdown(f"{data['passed']}/{data['total']} passed")
            if data["total"]:
                st.progress(data["passed"] / data["total"])
            if data["failed"] or data["warning"]:
                st.caption(f"⚠️ {data['failed']} failed, {data['warning']} warning")

st.divider()

# --- Failed test drill-downs --------------------------------------------
st.subheader("Failed test drill-downs")
any_failed = False
for dim_name, data in report["per_dimension"].items():
    for case in data["cases"]:
        if case["verdict"] == "fail":
            any_failed = True
            with st.expander(f"❌ [{dim_name}] {case['id']}"):
                st.markdown(f"**Question:** {case['question']}")
                st.markdown(f"**Expected:** {case['expected_answer']}")
                st.markdown(f"**Actual:** {case['actual_answer']}")
                st.markdown(f"**Root cause:** {case.get('root_cause') or '—'}")
                st.markdown(f"**Suggested fix:** {case.get('suggested_fix') or '—'}")
                if case.get("latency_seconds") is not None:
                    st.caption(f"Latency: {case['latency_seconds']:.2f}s")
if not any_failed:
    st.success("No failed test cases 🎉")

st.divider()

# --- RAGAS metrics ---------------------------------------------------------
st.subheader("RAGAS scores")
ragas_scores = report.get("ragas_scores", {})
if ragas_scores:
    cols = st.columns(len(ragas_scores))
    for col, (metric, value) in zip(cols, ragas_scores.items()):
        with col:
            st.markdown(f"**{metric.replace('_', ' ').title()}**")
            st.progress(min(max(value, 0.0), 1.0))
            st.caption(f"{value:.2f}")
    if report.get("ragas_diagnosis"):
        st.caption(f"💡 {report['ragas_diagnosis']}")
else:
    st.caption("RAGAS scores not available. Install `ragas` and `datasets`, "
               "then re-run `python run_evaluation.py`.")
